func TestEecptReceiver(t *testing.T) {
	sink := make(chan lp.CCMessage, 2000)
	r, err := NewEECPTReceiver("testreceiver", json.RawMessage(eecpt_config))
	if err != nil {
		t.Error(err.Error())
	}
	r.SetSink(sink)
	r.Start()
	t.Log("Receiver started")
	i := 0
	for _, m := range strings.Split(test_metrics, "\n") {
		if i == 0 {
			t.Log("Send 4 metrics")
		}
		mr := strings.NewReader(m)
		http.Post("http://localhost:8088/", "application/text", mr)
		i++
		if i >= 4 {
			time.Sleep(100 * time.Millisecond)
			i = 0
		}
	}
	r.Close()
}

